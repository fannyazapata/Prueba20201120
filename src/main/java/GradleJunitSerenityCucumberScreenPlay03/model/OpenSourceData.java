package GradleJunitSerenityCucumberScreenPlay03.model;

public class OpenSourceData {

	private String user;
	private String password;
	private String postTitle;
	private String pageTitle;
	private String notePost;
	private String notePages;
	
	public String getNotePages() {
		return notePages;
	}
	public void setNotePages(String notePages) {
		this.notePages = notePages;
	}
	public String getNotePost() {
		return notePost;
	}
	public void setNotePost(String notePost) {
		this.notePost = notePost;
	}
	public String getPageTitle() {
		return pageTitle;
	}
	public void setPageTitle(String pageTitle) {
		this.pageTitle = pageTitle;
	}
	public String getPostTitle() {
		return postTitle;
	}
	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}

}